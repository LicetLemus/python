# variables almacenas diferentes tipos de datos que pueden ser utilizados
#  en un programa y modificados en tiempo de ejecución.


mensaje = "hello world!" # mensaje (identificador unico) es una variable que almacena el string "hello world!".
mensaje = "adios" # modificamos el valor de la variable mensaje por "adios".

print(mensaje) # imprime en la terminal el valor de la variable mensaje que es adios.

mensaje = 45 # modificamos el valor de la variable mensaje por 45.

print(mensaje) # imprime en la terminal el valor de la variable mensaje que es 45.

