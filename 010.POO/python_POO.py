"""
Paradigma estruturado: es un paradigma de programación que se basa en la estructura de los datos, es decir, 
los datos son el elemento más importante de un programa. Se basa en la secuencia de instrucciones, se utilizan 
variables, estructuras de control, operadores.
"""

"""
Programación funcional: es un paradigma de programación que trata de resolver un problema a través de funciones.
dividiendo el problema en pequeñas funciones que se encargan de resolver una parte del problema. Creamos modulos
que contienen funciones que se encargan de resolver una parte del problema.
"""

"""
Programación orientada a objetos: es un paradigma de programación que se basa en la creación de clases y objetos.
donde una clase es un molde para crear objetos, y un objeto es una instancia de una clase. Las clases tienen atributos
que son las caracteristicas de los objetos y métodos que son las acciones que pueden realizar los objetos.
"""